// PERF FIX: Use Promise.all for parallel DB queries in getTeamStats
async function getTeamStats(teamId) {
  const [noteCount, team] = await Promise.all([
    Note.countDocuments({ teamId: new mongoose.Types.ObjectId(teamId), isDeleted: false }),
    Team.findById(teamId).select("members"),
  ]);
  return { noteCount, memberCount: team?.members.length || 0 };
}

